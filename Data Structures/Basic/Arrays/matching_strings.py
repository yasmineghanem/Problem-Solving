def matchingStrings(stringList, queries):
    # Write your code here
    occurrences = [0 for _ in queries]

    for idx, query in enumerate(queries):
        indices = [i for i, string in enumerate(stringList) if string == query]
        occurrences[idx] = len(indices)

    return occurrences

